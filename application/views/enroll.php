<h2><span class="mw-headline" id=""><a name="">构筑计算机人才“金字塔”，打造计算机教育“中华牌”</a></span></h2>
<p>十年前的3月，上海交通大学在美国夏威夷夺得ACM国际大学生程序设计竞赛世界冠军，这是亚洲有史以来该赛事的第一个世界冠军，也是上海交通大学计算机专业的本科生有史以来第一次站在世界舞台的最高点。
</p>
<p>庆贺之余，促使我们思考更深层的问题：我们的学生是否能够站在世界科学舞台的至高点？由此揭开了上海交通大学计算机科学与技术高端人才培养的序幕。
</p>

<h3><span class="mw-headline" id="-2"><a name="-2">班名由来</a></span></h3>



<p>上海交通大学计算机科学班（ACM班）的取名源于国际科学教育计算机组织——美国计算机学会（Association of Computing Machinery），它是世界上第一个、也是最有影响的计算机组织，计算机领域最高奖——图灵奖是由该组织设立和颁发的。这寓意ACM班旨在培养计算机科学家。
</p>

<h3><span class="mw-headline" id="-3"><a name="-3">班级理念</a></span></h3>



<p>ACM班崇尚“先做人、后做学问，在做学问中学做人”的教育理念，力求突破传统的教学模式，采取了一系列教育教学改革举措。第一，推行“思想-方法-实践”的素质教育体系，培养学生大志与责任、自主学习能力，形成研究型教学模式和课程体系，探索并实践精英人才培养模式。第二，实施“实验室-课堂-社会”的科研训练机制、多层次的实践环境、互动式的交流与讨论，培养学生的研究、实践、协调沟通等综合能力。第三，实行淘汰制和本硕连读制，激发学生学习的积极性和主动性。第四，联合微软亚洲研究院等国际知名企业，全面开展校企合作。
</p>

<h3><span class="mw-headline" id="-4"><a name="-4">班级成果</a></span></h3>



<p>十年来，ACM班学生获得了国内外荣誉和奖项数百人次：继2002年后2005、2010年又两次获得ACM国际大学生程序设计竞赛世界冠军，成为全球该赛事第三个“三冠王”；以第一作者身份在NIPS、WWW、SIGIR、SIGMOD、SIGKDD、ICML、AAAI等重要的国际会议和期刊上发表了40余篇学术论文，并获得国际会议最佳学生论文和最佳候选学生论文各1篇；2005、2009年两次获得国家级教学成果二等奖。
</p>

<h3><span class="mw-headline" id="-5"><a name="-5">班级未来</a></span></h3>



<p>2011年，ACM班已作为“上海交通大学基础学科拔尖学生培养实验计划”中的计算机科学班纳入致远学院。该班将由图灵奖获得者、康奈尔大学教授John Hopcroft领衔的国际知名教授组成讲席教授团共同执教与指导，全面实施“上海交通大学-国际知名大学-微软亚洲研究院”联合培养。
</p>
<p>计算机科学班（ACM班）的历史又翻开了新的一页，新征程、新挑战，我们共同预祝其拥有更加美好的明天！
</p>
